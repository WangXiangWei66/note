def  fun(a,b,c,d):
    print(a)
    print(b)
    print(c)
    print(d)

#调用时，不写参数名，叫位置 传参
fun(10,209,30,40)
print('----------------------------------------------')
# 在调用'时，写参数名，叫关键 字传参
fun(a=10,b=20,d=30,c=50)
print('----------------------------------------------')
# 即使用位置 传参也使用关键 字传参
fun(10,29,c=40,d=90)